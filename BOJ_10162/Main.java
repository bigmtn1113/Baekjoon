import java.util.Scanner;

public class Main {
	
	public static void main(String[] args){
		Scanner sc = new Scanner(System.in);
		
		int t = sc.nextInt();
		
		sc.close();
		
		int minute = t / 60;
		int a = minute / 5;
		int b = minute % 5;
		int c = t % 60;
		
		System.out.print((c % 10 == 0) ? (a + " " + b + " " + c / 10) : -1);
	}
	
}
